int	ft_sqrt(int n)
{
	int	i;

	i = -1;
	if (n < 0)
		return (i);
	while (++i < n)
	{
		if (i * i == n)
			return (i);
		if (i * i > n)
			return (--i);
	}
	return (i);
}
