#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	if (!lst)
		return (0);
	i = 0;
	while (lst)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}

void	*ft_getnblist(t_list *lst, int nb)
{
	int	i;

	if (nb >= ft_lstsize(lst))
		return (NULL);
	i = 0;
	while (i++ < nb)
		lst = lst->next;
	return (lst->content);
}
