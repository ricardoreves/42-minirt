#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	write(fd, "\n", 1);
}

void	ft_putendl(char *s)
{
	ft_putstr(s);
	write(1, "\n", 1);
}
